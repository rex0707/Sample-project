#include "Person.h"

#include <iostream>

namespace Rex
{

/*********************************************************************************************
 * @brief Construct a new Person object
 ********************************************************************************************/
Person::Person()
{}
/*********************************************************************************************
 * @brief Construct a new Person object
 * 
 * @param[in] Name Name of the person
 * @param[in] Age Age of the person
 ********************************************************************************************/
Person::Person( const std::string& Name, const unsigned int& Age ) : Name_(Name), Age_(Age)
{}
/*********************************************************************************************
 * @brief Destroy the Person object
 ********************************************************************************************/
Person::~Person()
{}
/*********************************************************************************************
 * @brief Show the name and age of the person
 ********************************************************************************************/
void Person::ShowInfo() const
{
  std::cout << "My name is " << Name_ << " and I'm " << Age_ << " years old." << std::endl;
}
/*********************************************************************************************
 * @brief Set the Name_ object
 ********************************************************************************************/
void Person::SetName( const std::string& Name )
{
  Name_ = Name;
}

/*********************************************************************************************
 * @brief Set the Age object
 ********************************************************************************************/
void Person::SetAge( const unsigned int& Age )
{
  Age_ = Age;
}

} // end of the namespace